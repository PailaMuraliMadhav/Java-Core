// WAP to print char from range A to M and X to M
class Whileex {
	public static void main(String[] args){
		char ch = 'A';
		while(ch > 0 && ch <= 'M' ){
			System.out.println(ch);
			ch++;

		}
		ch = 'X';
				while(ch >= 'X' && ch <= 'Z' ){
			System.out.println(ch);
			ch++;

		}

	}
}